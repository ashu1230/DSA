public class Pattern{
    public static void main(String[] args) {

        // Patterns........
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");
        
 
        // Variables...
        // integer..
        int a = 4;
        int b = 5;
        int c = a + b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        // Strings..
        String d = "ashutosh";
        String e = "maurya";
        System.out.println(d + " " + e);


        System.out.println(d + " " + c + " " + e);
    }
}