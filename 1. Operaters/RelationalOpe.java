package Operaters;

public class RelationalOpe {
    public static void main(String arg[]) {
        

        // relational operater...
        int a = 10;
        int b = 5;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
